    private void tbUsersMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbUsersMouseReleased
        // TODO add your handling code here:
        int fila = tbUsers.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una Fila");
        } else {
            id_user = Integer.parseInt(tbUsers.getValueAt(fila, 0).toString());
            txtCodigo.setText(tbUsers.getValueAt(fila, 0).toString());
            txtNombre.setText(tbUsers.getValueAt(fila, 1).toString());
            txtApellidos.setText(tbUsers.getValueAt(fila, 2).toString());
            txtUsuario.setText(tbUsers.getValueAt(fila, 3).toString());
            txtContraseña.setText(tbUsers.getValueAt(fila, 4).toString());
            txtEstado.setText(tbUsers.getValueAt(fila, 5).toString());
            txtRol.setText(tbUsers.getValueAt(fila, 6).toString());
            txtCodEmpleado.setText(tbUsers.getValueAt(fila, 7).toString());
    }
    }//GEN-LAST:event_tbUsersMouseReleased

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        String mensaje = "";
        int fila = tbUsers.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe selecionar una Fila");
        } else {
            mensaje = users.actualizar(id_user, txtNombre.getText(), txtApellidos.getText(), txtUsuario.getText(),txtContraseña.getText(),
                    txtEstado.getText(), Integer.parseInt(txtRol.getText()), Integer.parseInt(txtCodEmpleado.getText()));
        }
        limpiarTabla();
        listar();
        lblMensaje.setText(mensaje);
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String mensaje = "";
        int fila = tbUsers.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una Fila");
        } else {
            mensaje = users.eliminar(id_user);
        }
        limpiarTabla();
        listar();
        btnNuevoActionPerformed(evt);
        lblMensaje.setText(mensaje);
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
        // TODO add your handling code here:
        if (!txtBuscar.getText().isEmpty()) {
            id_user = Integer.parseInt(txtBuscar.getText());
            usersDTO = users.buscar(id_user);
            if (usersDTO != null) {
                limpiarTabla();
                modelo = (DefaultTableModel) tbUsers.getModel();
                Object[] ob = new Object[11];
                ob[0] = usersDTO.getId_user();
                ob[1] = usersDTO.getUsers_name();
                ob[2] = usersDTO.getUsers_lastname();
                ob[3] = usersDTO.getLogin_user();
                ob[4] = usersDTO.getLogin_password();
                ob[5] = usersDTO.getUser_status();
                ob[6] = usersDTO.getId_rol();
                ob[7] = usersDTO.getId_employee();
                modelo.addRow(ob);
                tbUsers.setModel(modelo);
                lblMensaje.setText("Producto Encontrado");
            } else {
                lblMensaje.setText("Producto No encontrado");
                limpiarTabla();
            }
        } else {
            limpiarTabla();
            listar();
            lblMensaje.setText("");
        }
    }//GEN-LAST:event_txtBuscarKeyReleased

