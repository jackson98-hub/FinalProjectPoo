    private void limpiarTabla() {
        modelo = (DefaultTableModel) tbSale.getModel();
        modelo.getDataVector().removeAllElements();
        tbSale.removeAll();
    }
    private void limpiarTexto() {
        txtCodigo.setText("");
        txtBuscar.setText("");
        txtTipoPago.setText("");
        txtComprobante.setText("");
        txtTotal.setText("");
        txtEstado.setText("");
        txtCodUsuario.setText("");
        txtCodCliente.setText("");
        txtCodigo.requestFocus();
    }
    private void listar() {
        try {
            modelo = (DefaultTableModel) tbSale.getModel();
            Object[] ob = new Object[8];
            for (SaleDTO p : sale.listar()) {
                ob[0] = p.getId_sale();
                ob[1] = p.getType_of_payment();
                ob[2] = p.getNumber_proof_payment();
                ob[3] = p.getTotal();
                ob[4] = p.getSale_status();
                ob[5] = p.getId_user();
                ob[6] = p.getId_customer();
                modelo.addRow(ob);
            }
            tbSale.setModel(modelo);
        } catch (Exception e) {
        }
        
    }

