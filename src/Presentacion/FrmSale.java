    private void tbSaleMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbSaleMouseReleased
        // TODO add your handling code here:
        int fila = tbSale.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una Fila");
        } else {
            id_sale = Integer.parseInt(tbSale.getValueAt(fila, 0).toString());
            txtCodigo.setText(tbSale.getValueAt(fila, 0).toString());
            txtTipoPago.setText(tbSale.getValueAt(fila, 1).toString());
            txtComprobante.setText(tbSale.getValueAt(fila, 2).toString());
            txtTotal.setText(tbSale.getValueAt(fila, 3).toString());
            txtEstado.setText(tbSale.getValueAt(fila, 4).toString());
            txtCodUsuario.setText(tbSale.getValueAt(fila, 5).toString());
            txtCodCliente.setText(tbSale.getValueAt(fila, 6).toString());    }
    }//GEN-LAST:event_tbSaleMouseReleased

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        String mensaje = "";
        int fila = tbSale.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe selecionar una Fila");
        } else {
            mensaje = sale.actualizar(id_sale, txtTipoPago.getText(), txtComprobante.getText(), Float.parseFloat(txtTotal.getText()),txtEstado.getText(),
                    Integer.parseInt(txtCodUsuario.getText()), Integer.parseInt(txtCodCliente.getText()));
        }
        limpiarTabla();
        listar();
        lblMensaje.setText(mensaje);
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String mensaje = "";
        int fila = tbSale.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una Fila");
        } else {
            mensaje = sale.eliminar(id_sale);
        }
        limpiarTabla();
        listar();
        btnNuevoActionPerformed(evt);
        lblMensaje.setText(mensaje);
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void txtBuscarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyReleased
        // TODO add your handling code here:
        if (!txtBuscar.getText().isEmpty()) {
            id_sale = Integer.parseInt(txtBuscar.getText());
            saleDTO = sale.buscar(id_sale);
            if (saleDTO != null) {
                limpiarTabla();
                modelo = (DefaultTableModel) tbSale.getModel();
                Object[] ob = new Object[7];
                ob[0] = saleDTO.getId_sale();
                ob[1] = saleDTO.getType_of_payment();
                ob[2] = saleDTO.getNumber_proof_payment();
                ob[3] = saleDTO.getTotal();
                ob[4] = saleDTO.getSale_status();
                ob[5] = saleDTO.getId_user();
                ob[6] = saleDTO.getId_customer();
                modelo.addRow(ob);
                tbSale.setModel(modelo);
                lblMensaje.setText("Producto Encontrado");
            } else {
                lblMensaje.setText("Producto No encontrado");
                limpiarTabla();
            }
        } else {
            limpiarTabla();
            listar();
            lblMensaje.setText("");
        }
    }//GEN-LAST:event_txtBuscarKeyReleased

